export function getBattleDialogue(bossNumber: number, moment: BattleMoment) {
  if (bossNumber === TOTAL_LEVELS) return finalBossLines[moment]
  return lines[moment][(bossNumber - 1) % lines[moment].length]
}

export function getHeroBattleDialogue(bossNumber: number, moment: BattleMoment) {
  const choices = heroLines[moment]
  return choices[(bossNumber - 1) % choices.length]
}
